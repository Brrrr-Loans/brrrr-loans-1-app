-- Table: public.user_profile
CREATE TABLE public.user_profile (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  email text NULL,
  email_verified boolean NULL,
  email_verified_at timestamp with time zone NULL,
  first_name text NULL,
  last_name text NULL,
  full_name text NULL,
  username text NULL,
  avatar_url text NULL,
  image_url text NULL,
  has_image boolean NULL,
  cell_phone text NULL,
  office_phone text NULL,
  office_phone_extension text NULL,
  role public.user_role_internal NULL,
  is_internal_yn boolean NOT NULL DEFAULT false,
  is_active_yn boolean NULL,
  is_banned boolean NULL,
  is_locked boolean NULL,
  create_organization_enabled boolean NULL,
  delete_self_enabled boolean NULL,
  activated_date timestamp with time zone NULL,
  deactivation_date timestamp with time zone NULL,
  invitation_date timestamp with time zone NULL,
  last_sign_in_at timestamp with time zone NULL,
  last_active_at timestamp with time zone NULL,
  legal_accepted_at timestamp with time zone NULL,
  website text NULL,
  contact_id bigint NULL,
  clerk_id text NULL,
  updated_at timestamp with time zone NULL DEFAULT (now() AT TIME ZONE 'utc'::text),
  created_at timestamp with time zone NULL DEFAULT (now() AT TIME ZONE 'utc'::text)
); 