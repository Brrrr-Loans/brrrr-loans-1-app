-- Table: public.property
CREATE TABLE public.property (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  address text NULL,
  address_street text NULL,
  address_suite_apt text NULL,
  address_city text NULL,
  address_state public.us_states NULL,
  address_state_long public.us_states_long NULL,
  address_postal_code text NULL,
  address_country text NULL,
  address_county text NULL,
  property_type public.property_type NULL,
  occupancy public.property_occupancy NULL,
  units integer NULL,
  year_built integer NULL,
  sq_footage_gla_aiv integer NULL,
  sq_footage_gla_arv integer NULL,
  sq_footage_lot_aiv integer NULL,
  sq_footage_lot_arv integer NULL,
  value_aiv_appraised numeric NULL,
  value_aiv_estimate numeric NULL,
  value_arv_appraised numeric NULL,
  value_arv_estimate numeric NULL,
  purchase_price numeric NULL,
  purchase_date date NULL,
  sale_price numeric NULL,
  sale_date date NULL,
  recently_renovated public.yes_no NULL,
  renovation_completed date NULL,
  renovation_cost numeric NULL,
  flood_zone public.yes_no NULL,
  inspection public.yes_no NULL,
  warrantability public.warrantability NULL,
  created_at timestamp with time zone NULL DEFAULT (now() AT TIME ZONE 'utc'::text),
  updated_at timestamp with time zone NULL DEFAULT (now() AT TIME ZONE 'utc'::text)
); 