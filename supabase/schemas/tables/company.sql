-- Table: public.company
CREATE TABLE public.company (
  co_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  co_name text NULL,
  co_logo text NULL,
  co_phone text NULL,
  co_fax text NULL,
  co_website text NULL,
  co_ein text NULL,
  co_entity_type public.entity_type NULL,
  co_date_established date NULL,
  co_ppb_address_street text NULL,
  co_ppb_address_suite_apt text NULL,
  co_ppb_address_city text NULL,
  co_ppb_address_state public.us_states NULL,
  co_ppb_address_state_long public.us_states_long NULL,
  co_ppb_address_postal_code text NULL,
  co_ppb_address_country public.country_enum NULL,
  co_bank_of_business_account text NULL,
  co_bank_account_balance numeric NULL,
  co_role public.company_role NULL,
  co_state_of_formation public.us_states NULL,
  co_state_of_formation_long public.us_states_long NULL,
  primary_guarantor_id bigint NULL,
  created_at timestamp with time zone NULL DEFAULT (now() AT TIME ZONE 'utc'::text),
  updated_at timestamp with time zone NULL DEFAULT (now() AT TIME ZONE 'utc'::text)
); 